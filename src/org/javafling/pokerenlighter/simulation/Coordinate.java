class Coordinate
{
    //the row (x) and column (y) of the coordinate
    public int x;
    public int y;
    
    /**
     * Constructor
     * @param x the row
     * @param y the column
     */
    public Coordinate(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
}